package day2;

public class Ternarysequence {

	public static void main(String[] args) {
				
				int a=10;
				int b=20;
				int c= a>b ? a++ : b++;
				
			//if condition is true them first block will run else second will run 
				        
				    
				System.out.println(a+ "   "+ b +"  " + c);


	}

}
